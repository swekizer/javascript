console.log("2" > 1);
console.log("3" < 1); //javascript will convert string to number then convert


console.log(null > 0);
console.log(null == 0);
console.log(null >= 0); 

console.log(undefined == 0); //always false
console.log(undefined > 0);
console.log(undefined >= 0);

// === this is strict check
// it will not only check for equality but also its datatype

console.log(2 === 2); //True
